#pragma region Player 구조체
struct Player
{
	string name; // 플레이어 이름
	int maxhp; // 최대 체력
	int hp; //체력
	int damage; // 공격력
	int skillDamage; // 스킬 데미지
	int isCasting; // 스킬 시전중인지 여부
	int curAction; // 현재 취하고있는 행동

	void init(string _name) // 구조체 초기화 함수
	{
		name = _name;
		maxhp = 15;
		hp = 15;
		damage = 1;
		skillDamage = 3;
		isCasting = false;
		curAction = 0;
	}
	int Attack() // 공격 함수, 공격력을 반환한다.
	{
		cout << name << "이 공격합니다. "<< endl;
		return damage;
	}
	void Hit(int _damage) // 히트 함수, _damage 만큼 피해를 입는다.
	{
		hp = hp - _damage < 0 ? 0 : hp - _damage;
		cout << name << "이 " << _damage << "의 피해를 입었습니다. (현재 체력 : " << hp << ")" << endl;
	}
	void Guard() // 방어 함수
	{
		cout << name << "가 방어에 성공했습니다. (현재 체력 : " << hp << ")" << endl;
	}
	void Casting() // 스킬 시전 함수
	{
		cout << name << "이 기를 모으고 있습니다 "<< endl;
		isCasting = true;
	}
	int Skill() // 스킬 사용 함수
	{
		cout << name << "이 스킬을 사용합니다. "<< endl;
		isCasting = false;
		return skillDamage;
	}

	void RandomAction() // 이번 행동을 선택하는 함수
	{
		// srand로 행동을 선택할때마다 다른 행동을 하도록 만든다.
		Sleep(1);
		srand(GetTickCount64());
		curAction = rand() % 3;
	}
};

