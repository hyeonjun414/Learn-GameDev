// 화면의 체력을 갱신하는 함수
void UpdateHp(vector<vector<int>>& arr, vector<Player*> players)
{
	int displayGap = 3;
	for (int i = 0; i < 2; i++)
	{
		for (int j = 0; j < players[i]->maxhp; j++)
		{
			if (j + 1 <= players[i]->hp)
				arr[2][j + displayGap] = 1;
			else
				arr[2][j + displayGap] = 0;
		}
		displayGap += 20;
	}
}

// 어떤 플레이어가 이겼는지 판단하는 함수
bool WhoWin(vector<Player*> players)
{
	if (players[0]->hp == 0)
	{
		cout << players[1]->name << "이 승리했습니다." << endl;
		return true;
	}
	else if (players[1]->hp == 0)
	{
		cout << players[0]->name << "이 승리했습니다." << endl;
		return true;
	}
	return false;
}

