// 두 플레이어의 행동에 따라 로직을 실행한다.
void Fight(vector<Player*> players)
{
	// 행동 선택
	for (int i = 0; i < 2; i++)
	{
		players[i]->RandomAction();
		// 플레이어의 현재 행동을 출력한다.
		switch (players[i]->curAction)
		{
		case 0: // 공격
			cout << "Player " << i + 1 << " : 공     격\t\t";
			break;
		case 1: // 방어
			cout << "Player " << i + 1 << " : 방     어\t\t";
			break;
		case 2: // 스킬
			if (players[i]->isCasting)
				cout << "Player " << i + 1 << " : 스킬 사용\t\t";
			else
				cout << "Player " << i + 1 << " : 기 모으기\t\t";
			break;
		}
		cout << endl;
	}
	cout << endl;

	// 로직 부분
	if (players[0]->curAction == 0) // 1p : 공격
	{
		switch (players[1]->curAction)
		{
		case 0: // 2p : 공격
			SceneNum = 1;
			// 서로 일반 데미지를 입는다.
			players[1]->Hit(players[0]->Attack());
			players[0]->Hit(players[1]->Attack());
			break;
		case 1: // 2p : 방어
			SceneNum = 2;
			// 2p가 방어에 성공한다.
			players[1]->Guard();
			break;
		case 2: // 2p : 스킬
			SceneNum = 3;
			if (players[1]->isCasting) // 2p : 스킬 사용
			{
				// 1p는 스킬 데미지,
				// 2p는 일반 데미지를 입는다.
				players[0]->Hit(players[1]->Skill());
				players[1]->Hit(players[0]->Attack());
			}
			else // 2p : 스킬 시전
			{
				// 2p가 스킬을 시전하고 일반 데미지를 입는다.
				players[1]->Casting();
				players[1]->Hit(players[0]->Attack());
			}
		}
	}
	else if (players[0]->curAction == 1) // 1p : 방어
	{
		switch (players[1]->curAction)
		{
		case 0: // 2p : 공격
			SceneNum = 4;
			// 1p는 방어에 성공한다.
			players[0]->Guard();
			break;
		case 1: // 2p : 방어
			SceneNum = 5;
			// 아무일도 일어나지 않는다.
			NoAction(players);
			break;
		case 2: // 2p : 스킬
			SceneNum = 6;
			if (players[1]->isCasting) // 2p
			{
				// 1p는 스킬 데미지를 입는다.
				players[0]->Hit(players[1]->Skill());
			}
			else
			{
				// 2p는 스킬 시전을 한다.
				players[1]->Casting();
			}
		}
	}
	else // 1p : 스킬
	{
		if (players[0]->isCasting) // 1p : 스킬 사용
		{
			switch (players[1]->curAction)
			{
			case 0: // 2p : 공격
				SceneNum = 7;
				// 1p는 일반 데미지를,
				// 2p는 스킬 데미지를 입는다.
				players[1]->Hit(players[0]->Skill());
				players[0]->Hit(players[1]->Attack());
				break;
			case 1: // 2p : 방어
				SceneNum = 8;
				// 2p는 스킬 데미지를 입는다.
				players[1]->Hit(players[0]->Skill());
				break;
			case 2: // 2p : 스킬
				SceneNum = 9;
				if (players[1]->isCasting) // 2p
				{
					// 서로 스킬 데미지를 입는다.
					players[0]->Hit(players[1]->Skill());
					players[1]->Hit(players[0]->Skill());
				}
				else
				{
					// 2p는 스킬 데미지를 입고 스킬을 시전한다.
					players[1]->Casting();
					players[1]->Hit(players[0]->Skill());
				}
			}
		}
		else // 1p : 스킬 시전
		{
			switch (players[1]->curAction)
			{
			case 0: // 2p : 공격
				SceneNum = 7;
				// 1p는 일반 데미지를 입고 스킬을 시전한다.
				players[0]->Hit(players[1]->Attack());
				players[0]->Casting();
				break;
			case 1: // 2p : 방어
				SceneNum = 8;
				// 1p는 스킬을 시전한다.
				players[0]->Casting();
				break;
			case 2: // 2p : 스킬
				SceneNum = 9;
				if (players[1]->isCasting) // 2p
				{
					// 1p는 스킬 데미지를 받고 스킬을 시전한다.
					players[0]->Hit(players[1]->Skill());
					players[0]->Casting();
				}
				else
				{ // 두 플레이어는 스킬을 시전한다.
					players[0]->Casting();
					players[1]->Casting();
				}
			}
		}
	}
	cout << endl;
	// 두 플레이어의 현재 체력상태를 표기한다.
	for (int i = 0; i < 2; i++)
		cout << "Player " + to_string(i + 1) + " 체력 ( " + to_string(players[i]->hp) + " / " +
		to_string(players[i]->maxhp) + " )\n";


}
